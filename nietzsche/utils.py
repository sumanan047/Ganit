from enum import Enum


class Dimension(Enum):
    """
    Description:
    ==================
    An enumeration class representing the dimensions of a space.

    Parameters:
    ============
    D: int
        Represents one dimension.
    DD: int
        Represents two dimensions.
    DDD: int
        Represents three dimensions.
    """
    D = 1
    DD = 2
    DDD = 3
